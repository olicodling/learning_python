# Printing a number
print(2) 

# Printing a decimal
print(2.0987)

# Printing a negative
print(-2.0987)

# Divide numbers and print the remainder
print(10 % 3)

# Store a number as a variable and print it
my_number = 10.1
print(my_number)

# Convert a number into a string
print(str(my_number))

# Print a number and a string
print(str(my_number) + " is my favourite number")

# Function abs - Absolute value of a number
print(abs(my_number))

# Function pow - To the power of
print(pow(my_number, 10))

# Function max - Return the larger of the numbers passed to the funtion
print(max(my_number, 99))

# Function min - Return the smaller of the numbers passed to the funtion
print(min(my_number, 99))

# Function round - Round the number
print(round(3.9))

# Importing functions from a module. Math is the module. The below will import all math functions
from math import *

# Function floor - Round the number down the nearest integer 3.7 -> 3
print(floor(my_number))

# Function ceil - Round the number up the nearest integer 3.1 -> 4
print(ceil(my_number))

# Function sqrt - Find the square root of a number
print(sqrt(my_number))