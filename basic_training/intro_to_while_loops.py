# While loops allow us to execute specific code multiple times until a condition is false/true

# Setting the value of i to 1
i = 10

# This loop statement will print I IF if its less that or equal to 10. 
# Once it reaches 10, it'll continue & therefore print Done with loop.
while i <= 10:
    print(i)
 #   i = i + 1
    i += 1
    
print("Done with loop")